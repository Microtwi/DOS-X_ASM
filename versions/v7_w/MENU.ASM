; IO.ASM

ShowMenu PROC

draw_menu:

    call _clear_screen
    LEA DX, prompt_menu
    CALL PrintString

    mov ah, 01h
    int 21h

    CMP AL, '1'
    JE _show_draw_menu

    CMP AL, '2'
    JE _draw_credit

    CMP AL, '3'
    JE _program_end

    jmp draw_menu

_draw_credit:
    call _clear_screen
    LEA DX, logo
    CALL PrintString

    mov ah, 01h
    int 21h

    jmp draw_menu

_program_end:
    call _clear_screen
    call _exit_program

ShowMenu ENDP
