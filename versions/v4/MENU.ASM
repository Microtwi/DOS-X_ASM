; IO.ASM

.CODE

ShowMenu PROC
    LEA DX, prompt_draw
    CALL PrintString
    CALL NewLine

    MOV CX, 1
    MOV SI, 0

READ_MENU_LOOP:
    CALL ReadChar
    MOV inputs[SI], AL
    CALL NewLine
    INC SI
    LOOP READ_MENU_LOOP

    LEA DX, message
    CALL PrintString

    MOV CX, 1
    MOV SI, 0

PRINT_LOOP:
    MOV DL, inputs[SI]
    CALL PrintChar
    MOV DL, ' '
    CALL PrintChar
    INC SI
    LOOP PRINT_LOOP
    
    CALL NewLine
    RET
ShowMenu ENDP