; MAIN.ASM

.MODEL SMALL
.STACK 100h

INCLUDE DATA.ASM
INCLUDE IO.ASM

.CODE
MAIN PROC
    MOV AX, @DATA
    MOV DS, AX

    MOV CX, 3
    MOV SI, 0
READ_LOOP:
    LEA DX, prompt
    CALL PrintString

    CALL ReadChar
    MOV inputs[SI], AL
    CALL NewLine

    INC SI
    LOOP READ_LOOP

    LEA DX, message
    CALL PrintString

    MOV CX, 3
    MOV SI, 0
PRINT_LOOP:
    MOV DL, inputs[SI]
    CALL PrintChar

    MOV DL, ' '
    CALL PrintChar

    INC SI
    LOOP PRINT_LOOP

    MOV AH, 4Ch
    INT 21h
MAIN ENDP
END MAIN
