; IO.ASM

.CODE
PrintString PROC
    MOV AH, 09h
    INT 21h
    RET
PrintString ENDP

ReadChar PROC
    MOV AH, 01h
    INT 21h
    RET
ReadChar ENDP

PrintChar PROC
    MOV AH, 02h
    INT 21h
    RET
PrintChar ENDP

NewLine PROC
    MOV AH, 09h
    LEA DX, newl
    INT 21h
    RET
NewLine ENDP

ExitProgram PROC
    MOV AH, 4Ch
    INT 21h
ExitProgram ENDP