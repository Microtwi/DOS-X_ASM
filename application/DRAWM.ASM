ShowDrawMenu PROC

show_draw_menu:     
    CALL _clear_screen

    LEA DX, prompt_draw
    CALL PrintString

    mov ah, 01h                                                                                                                                                                                                                                                                                                                                           
    int 21h

    CMP AL, '1'
    JE draw_line_menu

    CMP AL, '2'
    JE call_square_menu

    CMP AL, '3'
    JE draw_menu

    CMP AL, '4'
    JE _program_end                                                                                                                                                                                                                                                                                                                                           

    jmp show_draw_menu

call_square_menu:
    call draw_square_menu

